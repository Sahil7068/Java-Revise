package loopBasedCoding;

public class FirstOddEven {
    public static void main(String[] args) {

        int N = 10;

        for (int i = 1; i<=N; i++){
            if (i%2 == 0){
                System.out.print(i + " ");
            }

        }
        System.out.println();

        for (int i = 1; i<=N; i++){
            if (i%2 != 0){
                System.out.print(i + " ");
            }

        }










    }
}
